    private void add_bottonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_bottonActionPerformed
        try {
            // TODO add your handling code here:
            if(book_id.getText().isEmpty() ||title_.getText().isEmpty() || authername.getText().isEmpty() || publisher_.getText().isEmpty() || no_of_copies.getText().isEmpty())
                JOptionPane.showMessageDialog(null, "some Fields are empty");
            else if(!isNumeric(no_of_copies.getText()))
                JOptionPane.showMessageDialog(null, "you should enter a numeric value in the \"no of copies\"  field");
            else{
                boolean isAdded = librarianRoleB.addBook(book_id.getText(), title_.getText(), authername.getText(), publisher_.getText(), Integer.parseInt(no_of_copies.getText()));
                if(!isAdded)
                    JOptionPane.showMessageDialog(null, "the Book With id "+ book_id.getText()+ " already exists!");
                else {
                    //ŸçString bId = (book_id.getText());
                    book_id.setText("");
                    authername.setText("");
                    publisher_.setText("");
                    title_.setText("");
                    no_of_copies.setText("");
                    JOptionPane.showMessageDialog(null, "the Book With id "+ book_id.getText()+ " has been successfully added");  
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_add_bottonActionPerformed

